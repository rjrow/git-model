
# helper function for database field list R
generate_field_list <- function(table, main.list)
{
  matrix.names <- as.character(names(table))
  matrix.names <- gsub('[.]','_',matrix.names)
  tmp.lst <- main.list
  tmp.lst <- tmp.lst[matrix.names]
  return(tmp.lst)
}

